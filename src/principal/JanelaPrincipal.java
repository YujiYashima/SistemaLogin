    private void login(){
        if (new String(jPasswordField1.getPassword()).equals(key) && jTextField1.getText().equals(user)) {
            
            JanelaPrivada janelaPrivada = new JanelaPrivada();
            janelaPrivada.setVisible(true);
            this.dispose();
            
        } else {
            
            JOptionPane.showMessageDialog(this, "Senha ou Usuário inválidos", "ERROR", JOptionPane.ERROR_MESSAGE);
            
        }
        
    }
    
