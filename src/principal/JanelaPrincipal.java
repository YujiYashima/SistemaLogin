    private void jCheckBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox1ActionPerformed
        if (jCheckBox1.isSelected()) {
            
            jPasswordField1.setEchoChar('\u0000');
            jLabel2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/principal/ocultarSenha.png")));
            jCheckBox1.setText("Ocultar Senha"); 
           
        } else {
            
            jPasswordField1.setEchoChar(senha);
            jLabel2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/principal/exibirSenha.png")));
            jCheckBox1.setText("Mostrar Senha"); 
            
        }
    }//GEN-LAST:event_jCheckBox1ActionPerformed

